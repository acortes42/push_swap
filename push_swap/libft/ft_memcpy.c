#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*destination;
	unsigned char	*source;

	destination = (unsigned char *)dst;
	source = (unsigned char *)src;
	if (!n || dst == src)
		return (dst);
	while (n--)
		*destination++ = *source++;
	return (dst);
}
