#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;
	char	car;

	i = 0;
	str = (char *) s;
	car = (char) c;
	while (str[i] != '\0')
		i++;
	while (str[i] != car && i > 0)
		i--;
	if (str[i] == car)
		return (str + i);
	return (NULL);
}
